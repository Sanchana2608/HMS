    private void enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterActionPerformed
        String patientid= pid.getText();
        try{
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalmanagement","root", "root123");
            PreparedStatement ps = conn.prepareStatement("select patientname,patientnumber,email,dob,address from patientinfo where patientid=?");
            ps.setString(1,patientid);
            ResultSet rs = ps.executeQuery();
            if(rs.next())
            {
                name.setText(rs.getString(1));
                phno.setText(rs.getString(2));
                email.setText(rs.getString(3));
                dob.setText(rs.getString(4));
                address.setText(rs.getString(5));

            }
            else
            {

                name.setText(null);
                phno.setText(null);
                email.setText(null);
                dob.setText(null);
                address.setText(null);
                JOptionPane.showMessageDialog(null,"NOT A VALID Patient ID  !","Failed",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"cant search patient  !","Failed",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_enterActionPerformed

