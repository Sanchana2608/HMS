    public patientProfile(String pid) {
        initComponents();
        this.patientid=pid;
        try
        {
               Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalmanagement","root", "root123");
               PreparedStatement ps = conn.prepareStatement("select * from patientinfo where patientid=?");
               ps.setString(1,patientid);
               System.out.print(patientid);
               ResultSet rs = ps.executeQuery();
               while(rs.next())
               {
                  nametextfield.setText(rs.getString(2));
                  useridfield.setText(rs.getString(1));
                  addresstextfield.setText(rs.getString(6));
                  dob.setText(rs.getString(3));
                  
                  phnotextfield.setText(rs.getString(5));
                  emailtextfield.setText(rs.getString(4));
               
                }
               
        }
        catch(SQLException ex)
        {
            dispose();
            System.err.println ("error");
        } 
}
